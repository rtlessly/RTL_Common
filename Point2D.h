#ifndef _Point2D_h_
#define _Point2D_h_


//******************************************************************************
/// A struct that contains a 2D X-Y point. 
//******************************************************************************

struct Point2D
{
    float X;
    float Y;

    /***************************************************************************
    Constructors
    ***************************************************************************/
    public: Point2D() : X(0), Y(0) { };

    public: Point2D(float x, float y) : X(x), Y(y) { };
    
    /***************************************************************************
    Public implementation
    ***************************************************************************/
    
    public: float R() { return sqrt(X*X + Y*Y); };
    
    public: float Angle() { return (X == 0 && Y == 0) ? 0.0 : atan2(Y, X); };
};

#endif
